    String organizationIdentifier;
    String datasetType;

    public String getQueryId() {
        return queryId;
    }

    public void setQueryId(String queryId) {
        this.queryId = queryId;
    }

    public String getOrganizationIdentifier() {
        return organizationIdentifier;
    }

