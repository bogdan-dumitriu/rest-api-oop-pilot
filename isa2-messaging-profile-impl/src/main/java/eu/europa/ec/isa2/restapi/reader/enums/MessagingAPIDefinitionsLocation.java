package eu.europa.ec.isa2.restapi.reader.enums;

/**
 *  INLINE - definitions are generated inline where they are used
 *  DOCUMENT_COMPONENTS - definitions are generated in the document components #/componets/(schema|parameters|headers).
 *  MESSAGING_API_COMPONENTS - definitions are referenced to the published messagingAPI document.
 *  MESSAGING_API_OBJECT - definitions are referenced to the published json file for each object.
 */
public enum MessagingAPIDefinitionsLocation {
    INLINE,
    DOCUMENT_COMPONENTS,
    MESSAGING_API_COMPONENTS,
    MESSAGING_API_OBJECT;


}
